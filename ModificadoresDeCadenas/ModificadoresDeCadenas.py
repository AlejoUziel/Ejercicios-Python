# strip() eliminara los espacios en una cadena.
a=str("   hola Alejandro  ")
print(a.strip()) # eliminira los espacios que estan delante y detras del texto.

# replace() reemplaza una cadena con otra cadena.
b=str("komo estas?")
print(b.replace("k","c")) # reemplaza la letra k por la c.

#split() divide las cadenas a subcadenas 
c = "Ve a, dormir!"
print(c.split(",")) # dividira la cadena en dos cadenas.

